// @lc code=start
class Solution {
    public boolean judgeSquareSum(int c) {
        //解法1：双指针
        if (c < 0)
            return false;
        int left = 0;
        int right = (int) Math.sqrt(c);
        while (left <= right) {
            int sum = left * left + right * right;
            if (sum == c)
                return true;
            else if (sum < c)
                left++;
            else
                right--;
        }
        return false;
    }
}
